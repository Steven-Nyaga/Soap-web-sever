    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String admission_number = admission.getText();
        String student_name = name.getText();
        String student_course = course.getText();
        String student_year = year.getText();
        String student_semester = semester.getText();
        String student_email = email.getText();
        String student_phonenumber= phonenumber.getText();
        String student_address= address.getText();
        String student_code = code.getText();
        String entry_points = entry.getText();

        int adm_num = Integer.parseInt(admission_number);
        int phone   = Integer.parseInt(student_phonenumber);
        int stude_add   = Integer.parseInt(student_address);
        int entry_pnts   = Integer.parseInt(entry_points);
        int yr   = Integer.parseInt(student_year);
        int sem   = Integer.parseInt(student_semester);
        int stude_code = Integer.parseInt(student_code);

        GetStudent_Service service = new GetStudent_Service();
        final GetStudent proxy2 = service.getGetStudentPort();
        String result = proxy2.insertStudentDetails(adm_num,student_name,student_course,yr,sem,student_email,phone,stude_add,stude_code,entry_pnts);
        results.setText(result);
        if(result.equals("Success")){
            admission.setText("");
            name.setText("");
            course.setText("");
            year.setText("");
            semester.setText("");
            email.setText("");
            phonenumber.setText("");
            address.setText("");
            code.setText("");
            entry.setText("");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

