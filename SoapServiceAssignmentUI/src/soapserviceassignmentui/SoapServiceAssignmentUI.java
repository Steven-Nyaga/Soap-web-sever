    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String admission = admission_number.getText();
        int adm_num = Integer.parseInt(admission);
        GetStudent_Service service = new GetStudent_Service();
        final GetStudent proxy = service.getGetStudentPort();
        String result = proxy.studentID(adm_num);
        results.setText(result);
        if(!result.equals(null)){
            admission_number.setText("");
        }
    }//GEN-LAST:event_searchActionPerformed

